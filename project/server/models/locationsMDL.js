// Import Mongoose
// ============================================================================================================================
// ============================================================================================================================
const mongoose = require("mongoose");



// Create Location Schema
// ============================================================================================================================
// ============================================================================================================================
const locationSchema = mongoose.Schema({
    name: String,
    phone: String,
    address: {
        street: String,
        city: String,
        country: String,
        postcode: Number
    },
    hours: {
        sunday: [String],
        monday: [String],
        tuesday: [String],
        wednesday: [String],
        thursday: [String],
        friday: [String],
        saturday: [String]
    }
});



// Export Location Schema
// ============================================================================================================================
// ============================================================================================================================
module.exports = mongoose.model("Locations", locationSchema);