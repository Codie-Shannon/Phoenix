// Import dotenv, express, mongoose, and cors
// ============================================================================================================================
// ============================================================================================================================
require("dotenv").config();
const express = require("express");
const mongoose = require("mongoose");
const cors = require("cors");



// Initialize Express
// ============================================================================================================================
// ============================================================================================================================
const app = express();



// Get Port from Environment Variables
// ============================================================================================================================
// ============================================================================================================================
const port = process.env.port || 3000;



// Setup Middlewares
// ============================================================================================================================
// ============================================================================================================================
app.use(cors());
app.use(express.json());
app.use(express.urlencoded({ extended: true }));
app.use(express.static("uploads/icons"));
app.use(express.static("uploads/previews"));



// Connect to Database
// If the Database is Connected to Successfully, Display "MongoDB Connected" Message to Console
// Else if the Database is Not Connected to Successfully, Display Error to Console
// ============================================================================================================================
// ============================================================================================================================
mongoose.connect(process.env.DB_URI, {
    useNewUrlParser: true,
    useUnifiedTopology: true,
    //useFindAndModify: true,           // 
    useCreateIndex: true
})
.then(() => console.log("MongoDB Connectd"))
.catch(error => console.log(error));



// Set Routes Prefix
// ============================================================================================================================
// ============================================================================================================================
app.use("/api", require("./main"));



// Start Server
// ============================================================================================================================
// ============================================================================================================================
app.listen(port, () => console.log("Server Running on Port: ", port));



// Default Route
// ============================================================================================================================
// ============================================================================================================================
app.get('*', (req, res) => {
    // Display Error Message to User Informing Them That They Have Entered an Invalid Route
    res.status(404).send("You have entered an invalid route. Please enter a valid route to perform CRUD operations.");
});