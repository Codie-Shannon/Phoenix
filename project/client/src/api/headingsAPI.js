// Import Axios
// ============================================================================================================================
// ============================================================================================================================
// ============================================================================================================================
import axios from "axios";




// Initialize Variables
// ============================================================================================================================
// ============================================================================================================================
// ============================================================================================================================
const url = "/api/headings";




// Export Class
// ============================================================================================================================
// ============================================================================================================================
// ============================================================================================================================
export default class headingsAPI {
    // Get Heading By Page
    // Run getHeadingByPage Function to Get Database Document from Server Based on its Page =======================================
    // ============================================================================================================================
    static async getHeadingByPage(page) {
        // Get Heading from Server Based on its Page
        const res = await axios.get(`${url}/${page}`);

        // Return Heading from the Server
        return res.data;
    }
}