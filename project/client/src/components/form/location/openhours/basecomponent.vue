<!-- Setup Script -->
<!-- =================================================================
======================================================================
======================================================================
================================================================== -->
<script setup>
/* Import Style Sheet */
/* =================================================================
====================================================================
================================================================= */
import './style.css';
</script>





<!-- Template -->
<!-- =================================================================
======================================================================
======================================================================
================================================================== -->
<template>
    <!-- Container -->
    <!-- =================================================================
    ======================================================================
    ================================================================== -->
    <div class="openhours_container">
        <!-- Header -->
        <!-- =============================================================
        =============================================================== -->
        <h6 class="openhours_header">{{header}}</h6>



        <!-- Header Underline -->
        <!-- =============================================================
        =============================================================== -->
        <div id="openhours_underline"/>



        <!-- Content Group -->
        <!-- =============================================================
        =============================================================== -->
        <h2 id="openhours_content" v-show="content" v-for="(item, index) in contentList" v-bind:key="index">{{item}}</h2>
    </div>
</template>





<!-- Script -->
<!-- =================================================================
======================================================================
======================================================================
================================================================== -->
<script>
/* Export Default */
/* =================================================================
====================================================================
================================================================= */
export default {
  /* Properties */
  /* =================================================================
  ================================================================= */
  props: {
    /* Header */
    /* =============================================================== */
    header: { type: String, required: true },


    /* Content Group */
    /* =============================================================== */
    contentgroup: { type: Object, required: false }
  },



  /* Data */
  /* =================================================================
  ================================================================= */
  data() {
    return {
      /* Heading */
      /* =============================================================== */
      content: { },
    }
  },



  /* Computed */
  /* =================================================================
  ================================================================= */
  computed: {
    /* Content List */
    /* =============================================================== */
    contentList() {
      // Validate Content Group
      if(this.contentgroup) {
        // Format and Return Open Hours
        return [
          this.format("sunday", "Sun"),
          this.format("monday", "Mon"),
          this.format("tuesday", "Tue"),
          this.format("wednesday", "Wed"),
          this.format("thursday", "Thur"),
          this.format("friday", "Fri"),
          this.format("saturday", "Sat")
        ];
      }
    }
  },



  /* Methods */
  /* =================================================================
  ================================================================= */
  methods: {
      /* Format */
      /* =============================================================== */
      format(day, abr) {
        // Get Open Hours Array for Specific Day
        let content = this.contentgroup[day];

        // Format and Return Open Hours
        return `${abr} : ${content[0]} - ${content[1]}`;
      }
  }
};
</script>